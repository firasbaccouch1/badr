if (!function_exists('GetSimpleText')) {
    function GetSimpleText ($text) {
        $textWithoutBr = preg_replace('/<br\s*\/?>/i', ' ', $text);
        // Get the first 200 characters
        $trimmedText = substr($textWithoutBr, 0, 500);
        return $trimmedText .'...';
    }
